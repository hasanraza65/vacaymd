    <h5 class="mt-4">Risks of Telemedicine:</h5>
    <ol>
      <li>Technical Challenges: Telemedicine relies on stable internet connections and technological infrastructure.
        Technical glitches or connectivity issues can disrupt consultations and hinder the delivery of healthcare services,
        potentially compromising patient care.</li>
      <li>Lack of Physical Examination: Remote consultations do not allow healthcare providers to physically assess
        certain symptoms or perform hands-on examinations. This limitation may lead to incomplete diagnoses or delays in
        identifying certain conditions.</li>
      <li>Data Security and Privacy Concerns: The exchange of medical information and personal data during telemedicine
        consultations raises concerns about privacy and data security.</li>
      <li>Diagnostic Challenges: Some medical conditions may require complex diagnostic procedures or tests that cannot be
        conducted remotely. In such cases, telemedicine consultations may not provide the full range of diagnostic options
        available during in-person visits, potentially impacting the accuracy of diagnoses.</li>
    </ol>

